    public void actualizarTabla(List<Paquete> paquetes){
        DefaultTableModel dtm = new DefaultTableModel();
        tabla.setModel(dtm);
        dtm.addColumn("CODIGO");
        dtm.addColumn("DESCRIPCION");
        dtm.addColumn("ALTO");
        dtm.addColumn("ANCHO");
        dtm.addColumn("LARGO");
        dtm.addColumn("PESO");
        
        Object[] fila = new Object[6];
        for (int i = 0; i < paquetes.size(); i++) {
            fila[0] = paquetes.get(i).getCodigo();
            fila[1] = paquetes.get(i).getDescripcion();
            fila[2] = paquetes.get(i).getAlto();
            fila[3] = paquetes.get(i).getAncho();
            fila[4] = paquetes.get(i).getLargo();
            fila[5] = paquetes.get(i).getPeso();
            
            dtm.addRow(fila);
        }
        
    }
