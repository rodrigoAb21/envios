    private void tablaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaMouseClicked
        DefaultTableModel m = (DefaultTableModel) tabla.getModel();
        int fila = tabla.getSelectedRow();
        txt_cod.setText(m.getValueAt(fila, 0).toString());
        txt_desc.setText(m.getValueAt(fila, 1).toString());
        txt_alto.setText(m.getValueAt(fila, 2).toString());
        txt_ancho.setText(m.getValueAt(fila, 3).toString());
        txt_largo.setText(m.getValueAt(fila, 4).toString());
        txt_peso.setText(m.getValueAt(fila, 5).toString());
        
    }//GEN-LAST:event_tablaMouseClicked

    
